import Head from "next/head";
import { Inter } from "next/font/google";
import SidebarMessage from "@/components/ChatPage/SidebarMessage/SidebarMessage";
import Layout from "@/components/Layout";
import { ReactElement, useEffect, useState } from "react";
import { NextPageWithLayout } from "./_app";
import { useAuthState } from "react-firebase-hooks/auth";
import { auth } from "@/firebase";
import getInitialState from "@/utils/getInitialState";
import { useDispatch } from "react-redux";
import { setInitialState } from "@/modules/appSlice";
import Loading from "@/components/Loading";

const inter = Inter({ subsets: ["latin"] });

// import '@/styles/tailwind.min.css'
const Page: NextPageWithLayout = () => {
  const [user] = useAuthState(auth);
  const dispatch = useDispatch();
  const [isLoading, setLoading] = useState(false);

  useEffect(() => {
    setLoading(true);
    getInitialState(user?.uid)
      .then((initialData) => {
        dispatch(setInitialState(initialData));
        setLoading(false);
      })
      .catch((err) => console.log(err));
  }, []);

  return (
    <>
      <Loading isShow={isLoading} />
      <Head>
        <title>Chatapp 2.0</title>
        <meta name="description" content="Generated by create next app" />
        <meta name="viewport" content="width=device-width, initial-scale=1" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <div className="main flex-1 flex flex-col">
        <div className="hidden lg:block heading flex-2">
          <h1 className="text-3xl text-gray-700 mb-4"></h1>
        </div>

        <div className="flex-1 flex h-full">
          {/* <button onClick={handleTestMessage}>adada</button> */}
          <SidebarMessage />
        </div>
      </div>
    </>
  );
};

Page.getLayout = function getLayout(page: ReactElement) {
  return <Layout>{page}</Layout>;
};

export default Page;
